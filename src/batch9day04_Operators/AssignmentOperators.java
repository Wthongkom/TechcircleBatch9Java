package batch9day04_Operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		
		// +=, -=, *=, /=
		
		int A = 10;
		
		A += 5; // A = A + 5 => A = 10 + 5
		
		A += 10; // A = A + 10 => A = 15 + 10
		
		System.out.println(A); // 25
		
		A -= 5; // A = A - 5
		
		System.out.println(A); // 20
		
		A *= 5; // A = 20 * 5  
		
		System.out.println(A); // 100
		
		A /= 5; // A = 100 / 5
		
		System.out.println(A); // 20
		
		A /= 5; // a = 20 / 5
		
		System.out.println(A); // 4
		
		

	}

}
